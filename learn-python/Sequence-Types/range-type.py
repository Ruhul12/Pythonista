"""range type actions"""

#first perameter is start with zero and end till the given number 
r = range(5)
for i in r:
    print("The range value is:",i)

#second perameter is specify the limit of range
r1 = range(1,7)
for i in r1:
    print("The range value now:",i)

# the third perameter is step the given range value
r2 = range(1,20,3)
for i in r2:
    print("After step added in range:",i)

