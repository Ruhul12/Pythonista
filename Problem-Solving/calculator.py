firstValue = int(input('Enter Your 1st Value: '))
secondValue = int(input('Enter Your 2nd Value: '))

sum = firstValue + secondValue
sub = firstValue - secondValue
mul = firstValue * secondValue
div = firstValue / secondValue

print(f'Sum of {firstValue} and {secondValue} Is :',sum)
print(f'Sub of {firstValue} and {secondValue} Is :',sub)
print(f'Mul of {firstValue} and {secondValue} Is :',mul)
print(f'Div of {firstValue} and {secondValue} Is :',div)