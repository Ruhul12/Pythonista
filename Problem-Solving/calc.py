print('___CALCULATOR____')
firstValue = int(input('Enter your integer first value: '))
secondValue = int(input('Enter your integer second value: '))

sum = firstValue + secondValue
sub = firstValue - secondValue
mul = firstValue * secondValue
div = firstValue / secondValue

print(f'Addition of {firstValue} and {secondValue} is:  ', sum)
print(f'Subtraction of {firstValue} and {secondValue} is:  ', sub)
print(f'Multiplication of {firstValue} and {secondValue} is:  ', mul)
print(f'Division of {firstValue} and {secondValue} is:  ', div)
